package avis.sprite;

import avis.base.AException;

public class ASpriteException extends AException {

	public ASpriteException() {
		super();
	}

	public ASpriteException(String message, Throwable cause) {
		super(message, cause);
	}

	public ASpriteException(String message) {
		super(message);
	}

	public ASpriteException(Throwable cause) {
		super(cause);
	}

	/**
	 * serial version id
	 */
	private static final long serialVersionUID = 7692782034611350356L;

}

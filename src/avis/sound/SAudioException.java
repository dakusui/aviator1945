package avis.sound;

import avis.base.AException;

public class SAudioException extends AException {
	/**
	 * serial version
	 */
	private static final long serialVersionUID = -1786907576152857496L;

	public SAudioException() {
		super();
	}

	public SAudioException(String message, Throwable cause) {
		super(message, cause);
	}

	public SAudioException(String message) {
		super(message);
	}

	public SAudioException(Throwable cause) {
		super(cause);
	}

}
